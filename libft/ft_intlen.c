#include "libft.h"

int			ft_intlen(long long nbr)
{
	int			i;
	long long	p;

	i = 0;
	p = 10;
	while (i++ < 19)
	{
		if (nbr < p)
			return (i);
		p *= 10;
	}
	return (19);
}
